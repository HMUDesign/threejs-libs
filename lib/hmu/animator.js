var HMU = HMU || {};

HMU.Animator = function() {
	this.list = [];
}

HMU.Animator.prototype.add = function(object, property, config) {
	this.list.push(new HMU.Animation(object, property, config));
}

HMU.Animator.prototype.tick = function(time) {
	this.list = this.list.filter(function(item) {
		if(!item.time || time - item.time < item.config.duration) {
			return true;
		}
		else {
			item.tick(true);
			return false;
		}
	}, this);
	
	for(var i = 0; i < this.list.length; i++) {
		this.list[i].tick(time);
	}
}


HMU.Animation = function(object, property, config) {
	this.object = object;
	this.property = property;
	this.config = config;
	
	if(!this.config.easing || !HMU.Easing[this.config.easing]) {
		this.config.easing = 'linear';
	}
	
	if(typeof this.config.change === 'undefined') {
		if(typeof this.config.base === 'undefined') {
			this.config.base = this.object[this.property];
		}
		
		this.config.change = this.config.end - this.config.base || 0;
	}
	
	if(typeof this.config.base === 'undefined') {
		if(typeof this.config.end === 'undefined') {
			this.config.base = this.object[this.property];
		}
		else {
			this.config.base = this.config.end - this.config.change;
			delete this.config.end;
		}
	}
}

HMU.Animation.prototype.tick = function(time) {
	if(!this.time) this.time = time;
	
	if(time === true)
		time = this.config.duration;
	else
		time = time - this.time;
	
	this.object[this.property] = HMU.Easing[this.config.easing](time, this.config.base, this.config.change, this.config.duration);
}


